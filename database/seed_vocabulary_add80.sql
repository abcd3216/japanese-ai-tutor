-- 只插入資料庫裡還沒有的（用 japanese + level 判斷是否重複）
INSERT INTO vocabulary (japanese, hiragana, chinese, level, category)
SELECT n.japanese, n.hiragana, n.chinese, n.level, n.category
FROM @new AS n
WHERE NOT EXISTS (
    SELECT 1 FROM vocabulary AS v
    WHERE v.japanese = n.japanese AND v.level = n.level
);

